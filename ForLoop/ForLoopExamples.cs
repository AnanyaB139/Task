using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ForLoop
{
    class ForLoopExamples
    {
        static void Main(string[] args)
        {
            string  name = "flower";

            for (int i = 0; i <= 5; i++)
            {
                Console.WriteLine(name);
               
            }
            Console.ReadLine();
        }
    }
}
